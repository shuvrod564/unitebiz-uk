                <h3 class="h3 text-gray mb-3">What is a private limited company?</h3>
                <p>
                    Most of the companies in the UK are private limited companies – LTDs.
                </p>
                <p>
                    They are legal business forms with their own assets & liabilities. The ‘limited liability’ feature of these private companies protects its shareholders’ personal assets in any adverse business situations. The liabilities of the shareholders are limited to the value of their shares. Shares in private companies cannot be offered to the general public.
                </p>
                <p>
                    Private limited companies are incorporated with Companies House and are required to adopt legal documents - Memorandum of Association and Articles of Association at the time of incorporation of the company.
                </p>
                <p>
                    Private limited companies must have a minimum of one director (who must be a natural person). In most cases, the sole shareholder in the company will be director of the company. In his capacity as a company director, they have several statutory duties – one of the primary duties is to ensure the compliance of statutory requirements and ensure that an annual return is submitted to Companies house every year.
                </p>
                <p>
                    Appointment of a Company Secretary is not mandatory for the private limited companies. 
                </p>

                <h3 class="h3 text-gray mt-5 mb-3">What is a public limited company?</h3>
                <p>
                    Public limited companies – PLCs - are similar to private limited companies in many aspects, mainly in the sense that they are legally distinct entities with their own assets & and liabilities. 
                </p>
                <p>
                    The shares in a public company can be traded to the general public and their shares can be listed on a stock exchange. PLCs are the only type of company allowed to raise capital from this type of public investment.
                </p>
                <p>
                    Public limited companies are also incorporated with Companies House and are required to adopt legal documents - Memorandum of Association and Articles of Association at the time of incorporation of the company. Besides, they must comply with certain conditions in terms of minimum allotted share capital & paid-up share capital etc. which needs to be reflected in the Certificate of Commencement of Business, issued by Companies House.
                </p>
                <p>
                    PLCs must have at least 2 directors. And, a Company Secretary is also required by law.
                </p>

