                </p>
                <p>
                    The director(s) of a company are appointed by the shareholders to manage the day-to-day affairs of the company. 
                </p>
                <p>
                    The details of the director(s) declared at the company incorporation are mentioned in Companies House Form IN01 – the form used to apply to form a company.
                </p>
                <p>
                    Shareholders can appoint new director(s) any time after the company’s incorporation, and it needs to be notified to Companies House within 14 days.
                </p>
                <p>
                    It is important to consider potential conflicts of interest when making appointment of director(s), as this is a key area of governance.
                </p>
                
                <h2 class="text-primary mt-5 pt-lg-3 mb-3">Resignation of Director</h2>
                <p>
                    A director may resign from directorship or be removed by the shareholders by passing an ordinary resolution at a meeting of the company. 
                </p>
                <p>
                    <strong>Resignation:</strong> Resigning as a company director is more than just giving notice. There are several additional considerations for both the director and the company. 
                </p>
                <p>
                    <strong>Removal:</strong> Directors must act in accordance with their statutory and fiduciary duties, and in the best interests of the company for the benefit of the shareholders. Where the shareholders feel that a director is not performing his duties, they may wish to remove those directors. And, this process needs to comply with the requirements of the notice set out in the statue.
                     
                </p>
                <p>
                    <strong>Issues to be considered by a director before resigning from the directorship of a UK Company:</strong> 
                </p>
                <p>
                    If a director is also a shareholder in the company, the act of ‘resignation’ from the directorship may require them to sell their shares in the company. In this case, there may be specific provisions listed down in the Articles of Association or a Shareholder’s agreement etc. on how they can offer their shares for sale. 
                </p>
                <p>
                    You can contact our Customer Service Desk to understand more about the provisions in this regard in the <strong> Articles of Association or a Shareholder’s agreement</strong>. 
                </p>
                
                <p class="lead fw-bold mb-3">How does a director of a UK Company resign?</p>
                <p>
                    Firstly, a director should comply with the terms of their service agreement – Letter of appointment. A director should also comply with the provisions, if any, in the Articles of Association or a Shareholder’s agreement.  
                </p>
                
                <p>Notice of resignation should generally be given in writing.</p>
                <p>
                    UK limited companies shall notify Companies House about certain changes in the company. These include the resignation of company directors. A company must do this within 14 days of the date of the director resigns form the office. 
                </p>
                <p>
                    The company should also (1) update its register of directors (2) update its register of directors’ residential addresses (3) update its register of shareholders (in the case where the director was also a shareholder in the company, and sold their shares following his resignation), and (4) record the resignation in writing in the Minutes of the next board meeting.
                </p>
                <p>
                    If a sole individual director in a private limited company intends to resign form the office, then a replacement is required in compliance with the provisions of the Companies Act 2006. Failure to comply with those provisions can lead to ‘strike-off’ by Companies House. 
                </p>
