                <h2 class="h2 text-primary mt-5 pt-lg-3 mb-3">Shareholders & Shareholders’ Agreements</h2>
                <h3 class="h3 text-gray mb-3">Shareholders</h3>
                <p><strong>Shareholders</strong> are the people that own a company. They invest their money in the company by subscribing to the <strong>Shares</strong> of the company.</p>
                <p>
                    Both individuals and body corporates can be a shareholder in a company. 
                </p>
                <p>
                    The liabilities of the shareholder(s) are limited and the shareholders’ personal assets are protected from the company’s debts the in case of negative net worth of a limited company (LTDs or PLCs).
                </p>
                <p> 
                    Shareholders can appoint and remove directors, and can grant powers to the directors.  
                </p>
                <p>
                    Any person / body corporate can become a shareholder in a company after the formation of the company.  This is different to being a subscriber. A subscriber is the first member of the company, who details were added to the memorandum of association, and by doing so, they agreed to become part of the company. The name of the original subscriber(s) will remain on the Memorandum even if he/she transfers all the shares or even dies.
                </p>

                <p class="lead fw-bold mb-3">Can you be a shareholder and a company director?</p>
                <p>
                    Yes, the same person can have both roles. A shareholder can also be a director in the same company.
                </p>
                <p>
                    The shareholder is the owner of the company and has control over the directors who manages the day-to-day affairs of the company, and also entitled to proportionate share of dividends declared by the company. 
                </p>
                <p>
                    Company directors are appointed to the Board specifically to manage all the day-to-day operations of the business. The objective is to protect the interests of the company as a whole as well as the shareholders.
                </p>

                <h3 class="h3 text-gray mt-5 mb-3">Registration of the UK Company</h3>
                <p>
                    At the time of incorporation of the company, the first company directors are also appointed at the same time. 
                </p>
                <p>
                    The person or persons you nominate as directors are named in form IN01 and will then be deemed to have been appointed as your company's first directors.
                </p>
                <p>
                    All subsequent appointments through submission of form AP01, and new directors coming onboard will be governed by the company’s articles of association.
                </p>
                <p>
                    A director shall file a service address with Companies House as well as his/ her home address. It will be the service address that appears on the public record, and personal addresses will be kept private.
                </p>
                <p><strong>Conflict of Interest:</strong> A company director must also give general notice of any interests in any contracts held that involve the company. </p>
                <p>
                    You can speak to our Customer Services Desk to understand more about the regulations in this regard.
                </p>
                <p>
                    As a rule, new directors are given a copy of the company's memorandum and articles of association, and details of the business and affairs of the company, including board meeting minutes, and statutory report and accounts covering the past two years.
                </p>

                <h3 class="h3 text-gray mt-5 mb-3">Duties of the Company Directors</h3>
                <p>
                    Directors are responsible for ensuring the statutory compliances by the company. 
                </p>
                <p>
                    The general duties of the Director are -
                </p>
                <ul class="desc__list">
                    <li class="mb-3">Act within powers in accordance with the company’s constitution and to use those powers only for the purposes for which they were conferred</li>
                    <li class="mb-3">Promote the success of the company for the benefit of its members</li>
                    <li class="mb-3">Exercise independent judgement</li>
                    <li class="mb-3">Exercise reasonable care, skill and diligence</li>
                    <li class="mb-3">Avoid conflicts of interest</li>
                    <li class="mb-3">Not to accept benefits from third parties</li>
                    <li class="mb-3">Declare an interest in a proposed transaction or arrangement</li> 
